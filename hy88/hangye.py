def get_hangye_from_url(url):
    req = requests.get(url, headers=get_useragent())
    req_text = req.text
    soup = BeautifulSoup(req_text, "html.parser").body
    hy_soup = soup.find("div", "content").find("div", "box ad_L").find_all("a")
    hy_names = tuple([(i.get("href"), i.get_text()) for i in hy_soup if i])
    return hy_names


